    // search will return a int value, then use method to search by isbn, set index of found ISBN to getComicSearchSection(index) || getRenteeSection(index)
    public int SearchInput(String method, String input) {
        input = input.trim();
        // set first letter to uppercase
        input = input.substring(0, 1).toUpperCase() + input.substring(1).toLowerCase();
        int output = 0;

        if (method != null) {
            switch (method) {

                case "ISBN": {

                    output = SearchByISBN(input);
                    break;
                }
                case "Member_ID": {

                    output = SearchByMemberID(input);
                    break;
                }

            }
        } else {
            OUTPUT_LOGS.setText("Please add a search method.");
        }

        return output;
    }

    public int SearchByISBN(String query) {
        int index = 0;

        System.out.println("\nQuery for " + query + "\n");
        String queuedISBN;

        for (var i = 0; i < Sys.getComicListSize(); i++) {
            queuedISBN = Sys.getISBN(i);
            System.out.println(queuedISBN);
            if (queuedISBN.equals(query)) {
                index = i;
            }
        }

        // if index has not changed
        if (index == 0) {
            OUTPUT_LOGS.setText("No such ISBN " + query + " in the data!");
            System.out.println("No Such ISBN found.");
        } else {
            System.out.println("\nReturned index" + index + "\n");
        }

        return index;
    }

